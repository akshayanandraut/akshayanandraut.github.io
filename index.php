<script>
$(document).ready(function (){

	$('.inputText').focusout(function(){
		if($(this).val() != ""){	
			$('.username').addClass("anotherClass");		
		}
		else
			$('.username').removeClass("anotherClass");
	});
	
	$('.inputPass').focusout(function(){
		if($(this).val() != ""){	
